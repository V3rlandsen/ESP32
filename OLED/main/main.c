#define I2C_MASTER_SDA_IO GPIO_NUM_10
#define I2C_MASTER_SCL_IO GPIO_NUM_11

#define SCREEN_WIDTH 128
#define SCREEN_HEIGHT 64


void app_main(void)
{
    i2c_master_dev_handle_t i2c_dev_handle = NULL;

    i2c_master_bus_config_t i2c_bus_config = {
        .i2c_port = I2C_NUM_0,
        .sda_io_num = I2C_MASTER_SDA_IO,
        .scl_io_num = I2C_MASTER_SCL_IO,
        .clk_source = I2C_CLK_SRC_DEFAULT,
        .glitch_ignore_cnt = 7,
    };

    i2c_master_bus_handle_t i2c_bus_handle;
    ESP_ERROR_CHECK(i2c_new_master_bus(&i2c_bus_config, &i2c_bus_handle));

    i2c_device_config_t i2c_dev_config = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address = 0x3C,
        .scl_speed_hz = 400000,
    };

    ESP_ERROR_CHECK(i2c_master_bus_add_device(
        i2c_bus_handle,
        &i2c_dev_config,
        &i2c_dev_handle));

    ssd1306_handle_t screen = {0};
    ssd1306_config_t screen_config = {
        .i2c_dev_handle = i2c_dev_handle,
        .width = SCREEN_WIDTH,
        .height = SCREEN_HEIGHT,
    };
    //vTaskDelay(pdMS_TO_TICKS(100));
    ESP_ERROR_CHECK(ssd1306_init(&screen, &screen_config));
    //vTaskDelay(pdMS_TO_TICKS(100));
    ssd1306_clear_display(&screen);
    ESP_ERROR_CHECK(ssd1306_draw_string(&screen, 0, 0, "Hello World! Here is cat:", true));
    ESP_ERROR_CHECK(ssd1306_draw_bitmap(&screen, 40,10,46,55,cat46x55));
    
    
    ESP_ERROR_CHECK(ssd1306_update(&screen));
    while (true)
    {   


        
        vTaskDelay(pdMS_TO_TICKS(1000));

    }
}